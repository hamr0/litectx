function checkRecall(tag, recallMap) {
  log(`\n  ${tag}:`);
  for (const q of QUERIES) {
    const hits = recallMap[q];
    const top = hits[0];
    const body = top?.body ?? "";
    const readable = body.length > 0 && !body.includes("%PDF") && /[a-zA-Z]{4,}/.test(body);
    log(`    q="${q.slice(0, 32)}…" -> ${hits.length} hit(s); top.body ${body.length} chars; readable=${readable}`);
    assert(`${tag}: content-word query returns a hit`, hits.length > 0);
    assert(`${tag}: top hit body is readable extracted text (not %PDF)`, readable);
  }
}
