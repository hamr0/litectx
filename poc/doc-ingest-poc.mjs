const QUERIES = ["multi-step compounding redundant replay bench", "swim-lane console harvest real-work traces"];

async function indexAndRecall(rootName, fileBody) {
  const root = join(work, rootName);
  mkdirSync(root, { recursive: true });
  writeFileSync(join(root, "doc.md"), fileBody);
  const ctx = new LiteCtx({ root, dbPath: ":memory:" });
  await ctx.index();
  const res = {};
  for (const q of QUERIES) {
    const hits = await ctx.recall(q, { kind: "doc", body: true, log: false });
    res[q] = hits;
  }
  return res;
}

