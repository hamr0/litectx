/** PDF buffer -> flat-text markdown (lossy by nature: reading-order best-effort, no headings). */
async function pdfToMarkdown(buf, { maxPages = 2000 } = {}) {
  const doc = await getDocument({ data: new Uint8Array(buf), useSystemFonts: true }).promise;
  if (doc.numPages > maxPages) throw new Error(`pdf exceeds maxPages (${doc.numPages} > ${maxPages})`);
  const paras = [];
  for (let p = 1; p <= doc.numPages; p++) {
    const page = await doc.getPage(p);
    const content = await page.getTextContent();
    let line = "";
    for (const item of content.items) {
      line += item.str;
      if (item.hasEOL) {
        if (line.trim()) paras.push(line.trim());
        line = "";
      }
    }
    if (line.trim()) paras.push(line.trim());
  }
  return { md: paras.join("\n\n"), pages: doc.numPages };
}

/** DOCX buffer -> markdown (clean, structured: mammoth recovers headings/lists/emphasis). */
async function docxToMarkdown(buf) {
  const { value } = await mammoth.convertToMarkdown({ buffer: buf });
  return { md: value };
}

