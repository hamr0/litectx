// Minimal md -> HTML so LibreOffice emits real Heading styles (DOCX) + flowing text (PDF).
function mdToHtml(md) {
  const out = [];
  for (const raw of md.split("\n")) {
    const line = raw.trimEnd();
    const h = /^(#{1,6})\s+(.*)/.exec(line);
    if (h) out.push(`<h${h[1].length}>${esc(h[2])}</h${h[1].length}>`);
    else if (/^[-*]\s+/.test(line)) out.push(`<p>&#8226; ${esc(line.replace(/^[-*]\s+/, ""))}</p>`);
    else if (line.trim()) out.push(`<p>${esc(line)}</p>`);
  }
  return `<!doctype html><html><head><meta charset="utf-8"></head><body>${out.join("\n")}</body></html>`;
}
const esc = (s) => s.replace(/&/g, "&amp;").replace(/</g, "&lt;").replace(/>/g, "&gt;");

